import java.util.ArrayList;

public class WordPair {
    // Property
    public String first;
    public String second;

    // Constructor
    public WordPair(String first, String second) {
        this.first = first;
        this.second = second;

    }

    // Methods
    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
